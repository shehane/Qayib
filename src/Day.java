public class Day
{
    private int mesyach;
    private int chislo;
    private String denNedeli;

    public Day() {
        this.mesyach = mesyach;
        this.chislo = chislo;
        this.denNedeli = denNedeli;
    }

    public int getMesyach() {
        return mesyach;
    }

    public void setMesyach(int mesyach) {
        this.mesyach = mesyach;
    }

    public int getChislo() {
        return chislo;
    }

    public void setChislo(int chislo) {
        this.chislo = chislo;
    }

    public String getDenNedeli() {
        return denNedeli;
    }

    public void setDenNedeli(String denNedeli) {
        this.denNedeli = denNedeli;
    }
}
